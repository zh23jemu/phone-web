export function isHiddenContact(contact) {
    const name = String(contact?.name || '').trim();
    const phone = String(contact?.phone || '').replace(/\s/g, '');

    return HIDDEN_CONTACT_NAMES.includes(name) || HIDDEN_CONTACT_PHONES.includes(phone);
}
